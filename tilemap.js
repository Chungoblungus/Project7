    "#      ##    ##    ##      #",
    "###### ##### ## ##### ######",
    "~~~~~# ##### ## ##### #~~~~~",
    "~~~~~# ##    G     ## #~~~~~",
    "~~~~~# ## ######## ## #~~~~~",
    "###### ## #~~~~~~# ## ######",
    "~~~~~~    #~~~~~~#    ~~~~~~",
    "###### ## #~~~~~~# ## ######",
    "~~~~~# ## ######## ## #~~~~~",
    "~~~~~# ##    P     ## #~~~~~",
    "~~~~~# ## ######## ## #~~~~~",
